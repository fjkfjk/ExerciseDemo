        public static string DecryptStringFromBytes_Rsa(byte[] cipherText, string privateKey)
        {
            string decrypttext = "";
            using (RSACryptoServiceProvider crypt = new RSACryptoServiceProvider())
            {
                UTF8Encoding enc = new UTF8Encoding();
                byte[] bytes = cipherText;
                crypt.FromXmlString(privateKey);
                byte[] decryptbyte = crypt.Decrypt(bytes, false);
                decrypttext = enc.GetString(decryptbyte);
            }
            return decrypttext;
        }
