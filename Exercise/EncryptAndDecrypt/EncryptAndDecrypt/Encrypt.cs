        public static byte[] EncryptStringToBytes_Rsa(string plainText, string publicKey)
        {
            UTF8Encoding enc = new UTF8Encoding();
            byte[] bytes = enc.GetBytes(plainText);
            using (RSACryptoServiceProvider crypt = new RSACryptoServiceProvider())
            {
                crypt.FromXmlString(publicKey);
                bytes = crypt.Encrypt(bytes, false);
            }
            return bytes;
        }

